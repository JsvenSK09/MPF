    public void operasi_hitung()
    {
        switch (perhitungan)
        {
            case 1:
                ans = num + Double.parseDouble(Tempat_Angka.getText());
                Tempat_Angka.setText(Double.toString(ans));
                break;
                
            case 2:
                ans = num - Double.parseDouble(Tempat_Angka.getText());
                Tempat_Angka.setText(Double.toString(ans));
                break;
                
            case 3:
                ans = num * Double.parseDouble(Tempat_Angka.getText());
                Tempat_Angka.setText(Double.toString(ans));
                break;
                
            case 4:
                ans = num / Double.parseDouble(Tempat_Angka.getText());
                Tempat_Angka.setText(Double.toString(ans));
                break;    
        }    
    }
    
